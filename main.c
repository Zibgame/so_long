#include "so_long.h"

int	main(int argc, char **argv)
{
	t_game	game;
	if (check_arg(argc, argv) || !(check_map_valid(&argv[1]) == 0))
		return (1);
	game = start_game(argv[1]);
	mlx_loop(game.mlx);
	mlx_terminate(game.mlx);
	return (0);
}
